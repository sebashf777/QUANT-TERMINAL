st.subheader("10. Monte Carlo portfolio performance paths")

n_days = 252
n_samples = 100
sample_indices = np.random.choice(n_portfolios, n_samples, replace=False)
sample_weights = results[4:, sample_indices].T

daily_returns = np.random.multivariate_normal(
    mean_annual / 252,
    cov_annual / 252,
    size=n_days,
)

portfolio_values = np.ones((n_samples, n_days + 1))
for day in range(n_days):
    portfolio_values[:, day + 1] = portfolio_values[:, day] * (1 + sample_weights @ daily_returns[day])

fig, ax = plt.subplots(figsize=(10, 5))
for i in range(n_samples):
    ax.plot(portfolio_values[i], alpha=0.1, color="blue", linewidth=0.8)

