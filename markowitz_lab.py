st.subheader("4. Monte Carlo portfolios")

years = (pd.to_datetime(end_date) - pd.to_datetime(start_date)).days / 365.25
n_assets = len(available_tickers)

results = np.zeros((4 + n_assets, n_portfolios))

for i in range(n_portfolios):
    w = np.random.dirichlet(np.ones(n_assets))
    port_ret = float(np.dot(w, mean_annual))
    port_vol = float(np.sqrt(w @ cov_annual.values @ w))

    results[0, i] = port_ret
    results[1, i] = port_vol
    results[2, i] = (port_ret - risk_free_rate) / port_vol if port_vol > 0 else 0.0
    results[3, i] = (1 + port_ret) ** (1 / years) - 1
    results[4:, i] = w

# ------------------------------------------------------------------------------------
# 7. EFFICIENT FRONTIER (optimization)
# ------------------------------------------------------------------------------------
def portfolio_stats(weights, mean_ret, cov):
    port_ret = float(np.dot(weights, mean_ret))
    port_vol = float(np.sqrt(weights @ cov.values @ weights))
    return port_ret, port_vol

def minimize_volatility(mean_ret, cov):
    n = len(mean_ret)
    cons = ({"type": "eq", "fun": lambda x: np.sum(x) - 1},)
    bounds = tuple((0, 1) for _ in range(n))
    res = minimize(lambda w: portfolio_stats(w, mean_ret, cov)[1],
                   n * [1.0 / n], bounds=bounds, constraints=cons)
    return res.x

def maximize_sharpe(mean_ret, cov, rf):
    n = len(mean_ret)
    cons = ({"type": "eq", "fun": lambda x: np.sum(x) - 1},)
    bounds = tuple((0, 1) for _ in range(n))

    def neg_sharpe(w):
        pret, pvol = portfolio_stats(w, mean_ret, cov)
        return -((pret - rf) / pvol)

    res = minimize(neg_sharpe, n * [1.0 / n], bounds=bounds, constraints=cons)
    return res.x

min_vol_weights = minimize_volatility(mean_annual, cov_annual)
min_vol_ret, min_vol_vol = portfolio_stats(min_vol_weights, mean_annual, cov_annual)

tangency_weights = maximize_sharpe(mean_annual, cov_annual, risk_free_rate)
tangency_ret, tangency_vol = portfolio_stats(tangency_weights, mean_annual, cov_annual)

# Efficient frontier curve
target_returns = np.linspace(min_vol_ret, mean_annual.max() * 0.95, 150)
frontier_vols = []
frontier_rets = []

for target in target_returns:
    try:
        cons = [
            {"type": "eq", "fun": lambda x: np.sum(x) - 1},
            {"type": "eq", "fun": lambda x, tgt=target: portfolio_stats(x, mean_annual, cov_annual)[0] - tgt},
        ]
        bounds = tuple((0, 1) for _ in range(n_assets))
        x0 = min_vol_weights.copy()
        res = minimize(
            lambda w: portfolio_stats(w, mean_annual, cov_annual)[1],
            x0,
            bounds=bounds,
            constraints=cons,
            method="SLSQP",
            options={"ftol": 1e-9, "disp": False},
        )
        if res.success and res.fun > 0:
            ret, vol = portfolio_stats(res.x, mean_annual, cov_annual)
            if abs(ret - target) < 1e-6:
                frontier_vols.append(vol)
                frontier_rets.append(ret)
    except Exception:
        continue

frontier_vols = np.array(frontier_vols)
frontier_rets = np.array(frontier_rets)

